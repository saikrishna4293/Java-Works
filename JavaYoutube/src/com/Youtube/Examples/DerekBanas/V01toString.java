package com.Youtube.Examples.DerekBanas;

public class V01toString {

	public static void main(String[] args) {
		
		int i =10;
		String I_String = Integer.toString(i); 	//converting int to string
		System.out.println(I_String);
		i++;
		
		int i2 = Integer.parseInt(I_String);		//converting string to int
		System.out.println(i);
		System.out.println(i2);
		
		/*
		 * scsdsdada
		 * dsadsaa
		 * 
		 */
	}
}
